package Encapsulamneto;

public class Pet {
	String nomePet;
	String tipo;
	int idade;
	
	public Pet(String nomePet, String tipo, int idade) {
		this.nomePet = nomePet;
		this.tipo = tipo;
		this.idade = idade;
	}
	public String getNomePet() {
		return nomePet;
	}
	public void setNomePet(String nomePet) {
		this.setNomePet(nomePet);
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.setTipo(tipo);
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
	this.setIdade(idade);
	}
}
